public class Main
{
    public static void main(String[] args)
    {
        //switch case
        int day=3;
        switch(day)
        {
            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("Tuesday");
                break;
            case 3:
                System.out.println("wednesday");
                break;
            default:
                System.out.println("Another day!");
        }
    }
}